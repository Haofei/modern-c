grep -q 'store i32' "$OUT_DIR/joins.ll"
grep -q 'define i32 @load_ptr(ptr %p)' "$OUT_DIR/pointers.ll"
grep -q 'load i32, ptr %p' "$OUT_DIR/pointers.ll"
grep -q 'store i32 %value, ptr %p' "$OUT_DIR/pointers.ll"
grep -q 'store ptr %x.addr, ptr %p.addr' "$OUT_DIR/pointers.ll"
grep -q '@shared = global i32 7' "$OUT_DIR/globals.ll"
grep -q '@flag = global i1 0' "$OUT_DIR/globals.ll"
grep -q '@shared_ptr = global ptr @shared' "$OUT_DIR/globals.ll"
grep -q 'load i32, ptr @shared' "$OUT_DIR/globals.ll"
grep -q 'store i32 %value, ptr @shared' "$OUT_DIR/globals.ll"
grep -q 'alloca \[2 x i32\]' "$OUT_DIR/arrays.ll"
grep -q 'getelementptr inbounds \[2 x i32\]' "$OUT_DIR/arrays.ll"
grep -q 'call void @mc_trap_Bounds()' "$OUT_DIR/arrays.ll"
grep -q 'store ptr %t' "$OUT_DIR/arrays.ll"
grep -q 'alloca { i32, i32 }' "$OUT_DIR/structs.ll"
grep -q 'getelementptr inbounds { i32, i32 }' "$OUT_DIR/structs.ll"
grep -q 'store i32 %value, ptr %t' "$OUT_DIR/structs.ll"
grep -q 'store ptr %t' "$OUT_DIR/structs.ll"
grep -q '@values = global \[2 x i32\] \[i32 10, i32 20\]' "$OUT_DIR/aggregate_globals.ll"
grep -q '@pair = global { i32, i32 } { i32 1, i32 2 }' "$OUT_DIR/aggregate_globals.ll"
grep -q 'getelementptr inbounds \[2 x i32\], ptr @values' "$OUT_DIR/aggregate_globals.ll"
grep -q 'getelementptr inbounds { i32, i32 }, ptr @pair' "$OUT_DIR/aggregate_globals.ll"
grep -q 'define { i32, i32 } @make_pair()' "$OUT_DIR/aggregate_abi.ll"
grep -q 'define i32 @take_pair({ i32, i32 } %pair)' "$OUT_DIR/aggregate_abi.ll"
grep -q 'call { i32, i32 } @make_pair()' "$OUT_DIR/aggregate_abi.ll"
grep -q 'call i32 @take_pair({ i32, i32 }' "$OUT_DIR/aggregate_abi.ll"
grep -q 'define \[2 x i32\] @make_row()' "$OUT_DIR/aggregate_abi.ll"
grep -q 'call i32 @take_row(\[2 x i32\]' "$OUT_DIR/aggregate_abi.ll"
grep -q '@matrix = global \[2 x \[2 x i32\]\]' "$OUT_DIR/nested_aggregates.ll"
grep -q '@box = global { { i32, i32 } }' "$OUT_DIR/nested_aggregates.ll"
grep -q 'getelementptr inbounds \[2 x \[2 x i32\]\]' "$OUT_DIR/nested_aggregates.ll"
grep -q 'getelementptr inbounds { { i32, i32 } }' "$OUT_DIR/nested_aggregates.ll"
grep -q 'getelementptr inbounds { i32, i32 }' "$OUT_DIR/nested_aggregates.ll"
grep -q 'declare { ptr, i64 } @make_u8_slice()' "$OUT_DIR/slices.ll"
grep -q 'define i8 @read_slice({ ptr, i64 } %xs, i64 %i)' "$OUT_DIR/slices.ll"
grep -q 'define { ptr, i64 } @same_slice({ ptr, i64 } %xs)' "$OUT_DIR/slices.ll"
grep -q 'extractvalue { ptr, i64 }' "$OUT_DIR/slices.ll"
grep -q 'insertvalue { ptr, i64 } undef, ptr' "$OUT_DIR/slices.ll"
grep -q 'getelementptr inbounds i8, ptr' "$OUT_DIR/slices.ll"
grep -q 'icmp ule i64' "$OUT_DIR/slices.ll"
grep -q 'switch i32 %n' "$OUT_DIR/scalar_switch.ll"
grep -q 'i32 1, label %switch_arm' "$OUT_DIR/scalar_switch.ll"
grep -q 'i32 2, label %switch_arm' "$OUT_DIR/scalar_switch.ll"
grep -q 'switch i8 %c' "$OUT_DIR/scalar_switch.ll"
grep -q 'i8 65, label %switch_arm' "$OUT_DIR/scalar_switch.ll"
grep -q 'i8 10, label %switch_arm' "$OUT_DIR/scalar_switch.ll"
grep -q 'switch i32 %n, label %switch_arm' "$OUT_DIR/scalar_switch.ll"
grep -q 'i32 -1, label %switch_arm' "$OUT_DIR/scalar_switch.ll"
grep -q 'br label %for_cond' "$OUT_DIR/for_loops.ll"
grep -q 'for_body' "$OUT_DIR/for_loops.ll"
grep -q 'for_step' "$OUT_DIR/for_loops.ll"
grep -q 'for_end' "$OUT_DIR/for_loops.ll"
grep -q 'getelementptr inbounds i32, ptr' "$OUT_DIR/for_loops.ll"
grep -q 'getelementptr inbounds \[4 x i32\]' "$OUT_DIR/for_loops.ll"
grep -q 'call { ptr, i64 } @make_u32_slice()' "$OUT_DIR/for_loops.ll"
grep -q 'br label %while_end' "$OUT_DIR/for_loops.ll"
grep -q 'br label %while_cond' "$OUT_DIR/for_loops.ll"
grep -q ' and i32 ' "$OUT_DIR/scalar_expressions.ll"
grep -q ' or i32 ' "$OUT_DIR/scalar_expressions.ll"
grep -q ' xor i32 ' "$OUT_DIR/scalar_expressions.ll"
grep -q 'shl i32' "$OUT_DIR/scalar_expressions.ll"
grep -q 'lshr i32' "$OUT_DIR/scalar_expressions.ll"
grep -q 'zext i32' "$OUT_DIR/scalar_expressions.ll"
grep -q 'trunc i64' "$OUT_DIR/scalar_expressions.ll"
grep -q 'call void @mc_trap_InvalidShift()' "$OUT_DIR/scalar_expressions.ll"
grep -q 'call void @mc_trap_IntegerOverflow()' "$OUT_DIR/scalar_expressions.ll"
grep -q 'declare void @consume_u32(i32)' "$OUT_DIR/statement_workflow.ll"
grep -q 'call void @consume_u32(i32 %value)' "$OUT_DIR/statement_workflow.ll"
grep -q 'call i32 @combine(i32 %left, i32 %right)' "$OUT_DIR/statement_workflow.ll"
grep -q 'load i32, ptr %out.addr' "$OUT_DIR/statement_workflow.ll"
grep -q 'call void @mc_trap_IntegerOverflow()' "$OUT_DIR/statement_workflow.ll"
grep -q 'call \[4 x i32\] @make_values(i32 %seed)' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'call { \[4 x i32\], { ptr, i64 } } @make_bag(i32 %seed)' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'store \[4 x i32\] %t' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'store { \[4 x i32\], { ptr, i64 } } %t' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'getelementptr inbounds \[4 x i32\]' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'extractvalue { ptr, i64 }' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'for_body' "$OUT_DIR/aggregate_rvalues.ll"
grep -q 'declare void @mc_trap_Assert()' "$OUT_DIR/bool_conditions.ll"
grep -q 'logic_and_rhs' "$OUT_DIR/bool_conditions.ll"
grep -q 'logic_or_rhs' "$OUT_DIR/bool_conditions.ll"
grep -q 'call void @mc_trap_Assert()' "$OUT_DIR/bool_conditions.ll"
grep -q 'call i32 @next_value()' "$OUT_DIR/bool_conditions.ll"
grep -q 'call void @consume_bool(i1' "$OUT_DIR/bool_conditions.ll"
