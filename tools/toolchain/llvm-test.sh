"$MCC" emit-llvm tests/llvm/aggregate_abi.mc > "$OUT_DIR/aggregate_abi.ll"
llvm-as "$OUT_DIR/aggregate_abi.ll" -o "$OUT_DIR/aggregate_abi.bc"
"$MCC" emit-llvm tests/llvm/nested_aggregates.mc > "$OUT_DIR/nested_aggregates.ll"
llvm-as "$OUT_DIR/nested_aggregates.ll" -o "$OUT_DIR/nested_aggregates.bc"
"$MCC" emit-llvm tests/llvm/slices.mc > "$OUT_DIR/slices.ll"
llvm-as "$OUT_DIR/slices.ll" -o "$OUT_DIR/slices.bc"
"$MCC" emit-llvm tests/llvm/scalar_switch.mc > "$OUT_DIR/scalar_switch.ll"
llvm-as "$OUT_DIR/scalar_switch.ll" -o "$OUT_DIR/scalar_switch.bc"
"$MCC" emit-llvm tests/llvm/for_loops.mc > "$OUT_DIR/for_loops.ll"
llvm-as "$OUT_DIR/for_loops.ll" -o "$OUT_DIR/for_loops.bc"
"$MCC" emit-llvm tests/llvm/scalar_expressions.mc > "$OUT_DIR/scalar_expressions.ll"
llvm-as "$OUT_DIR/scalar_expressions.ll" -o "$OUT_DIR/scalar_expressions.bc"
"$MCC" emit-llvm tests/llvm/statement_workflow.mc > "$OUT_DIR/statement_workflow.ll"
llvm-as "$OUT_DIR/statement_workflow.ll" -o "$OUT_DIR/statement_workflow.bc"
"$MCC" emit-llvm tests/llvm/aggregate_rvalues.mc > "$OUT_DIR/aggregate_rvalues.ll"
llvm-as "$OUT_DIR/aggregate_rvalues.ll" -o "$OUT_DIR/aggregate_rvalues.bc"
"$MCC" emit-llvm tests/llvm/bool_conditions.mc > "$OUT_DIR/bool_conditions.ll"
llvm-as "$OUT_DIR/bool_conditions.ll" -o "$OUT_DIR/bool_conditions.bc"

grep -q '@llvm.uadd.with.overflow.i32' "$OUT_DIR/smoke.ll"
grep -q 'call void @mc_trap_IntegerOverflow()' "$OUT_DIR/smoke.ll"
! grep -q ' nsw ' "$OUT_DIR/smoke.ll"
! grep -q ' nuw ' "$OUT_DIR/smoke.ll"
grep -q 'switch i1 %flag' "$OUT_DIR/bool_switch.ll"
grep -q 'call void @mc_trap_InvalidRepresentation()' "$OUT_DIR/bool_switch.ll"
grep -q 'icmp ugt i32 %x, 10' "$OUT_DIR/bool_switch.ll"
grep -q 'alloca i32' "$OUT_DIR/loops.ll"
grep -q 'store i32' "$OUT_DIR/loops.ll"
grep -q 'load i32' "$OUT_DIR/loops.ll"
grep -q 'br label %while_cond' "$OUT_DIR/loops.ll"
grep -q '@llvm.usub.with.overflow.i32' "$OUT_DIR/loops.ll"
grep -q 'call void @mc_trap_DivideByZero()' "$OUT_DIR/div_rem.ll"
grep -q 'call void @mc_trap_IntegerOverflow()' "$OUT_DIR/div_rem.ll"
grep -q 'sdiv i32' "$OUT_DIR/div_rem.ll"
grep -q 'srem i32' "$OUT_DIR/div_rem.ll"
grep -q 'udiv i32' "$OUT_DIR/div_rem.ll"
grep -q 'urem i32' "$OUT_DIR/div_rem.ll"
