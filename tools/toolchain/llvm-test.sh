#!/usr/bin/env sh
set -eu

MCC="${1:?usage: llvm-test.sh path/to/mcc}"
OUT_DIR="${2:-zig-out/llvm-test}"

mkdir -p "$OUT_DIR"
"$MCC" emit-llvm tests/c_emit/smoke.mc > "$OUT_DIR/smoke.ll"
llvm-as "$OUT_DIR/smoke.ll" -o "$OUT_DIR/smoke.bc"
"$MCC" emit-llvm tests/llvm/bool_switch.mc > "$OUT_DIR/bool_switch.ll"
llvm-as "$OUT_DIR/bool_switch.ll" -o "$OUT_DIR/bool_switch.bc"
"$MCC" emit-llvm tests/llvm/loops.mc > "$OUT_DIR/loops.ll"
llvm-as "$OUT_DIR/loops.ll" -o "$OUT_DIR/loops.bc"
"$MCC" emit-llvm tests/llvm/div_rem.mc > "$OUT_DIR/div_rem.ll"
llvm-as "$OUT_DIR/div_rem.ll" -o "$OUT_DIR/div_rem.bc"
"$MCC" emit-llvm tests/llvm/joins.mc > "$OUT_DIR/joins.ll"
llvm-as "$OUT_DIR/joins.ll" -o "$OUT_DIR/joins.bc"
"$MCC" emit-llvm tests/llvm/pointers.mc > "$OUT_DIR/pointers.ll"
llvm-as "$OUT_DIR/pointers.ll" -o "$OUT_DIR/pointers.bc"
"$MCC" emit-llvm tests/llvm/globals.mc > "$OUT_DIR/globals.ll"
llvm-as "$OUT_DIR/globals.ll" -o "$OUT_DIR/globals.bc"
"$MCC" emit-llvm tests/llvm/arrays.mc > "$OUT_DIR/arrays.ll"
llvm-as "$OUT_DIR/arrays.ll" -o "$OUT_DIR/arrays.bc"
"$MCC" emit-llvm tests/llvm/structs.mc > "$OUT_DIR/structs.ll"
llvm-as "$OUT_DIR/structs.ll" -o "$OUT_DIR/structs.bc"
"$MCC" emit-llvm tests/llvm/aggregate_globals.mc > "$OUT_DIR/aggregate_globals.ll"
llvm-as "$OUT_DIR/aggregate_globals.ll" -o "$OUT_DIR/aggregate_globals.bc"
"$MCC" emit-llvm tests/llvm/aggregate_abi.mc > "$OUT_DIR/aggregate_abi.ll"
llvm-as "$OUT_DIR/aggregate_abi.ll" -o "$OUT_DIR/aggregate_abi.bc"

grep -q '@llvm.uadd.with.overflow.i32' "$OUT_DIR/smoke.ll"
grep -q 'call void @mc_trap_IntegerOverflow()' "$OUT_DIR/smoke.ll"
! grep -q ' nsw ' "$OUT_DIR/smoke.ll"
! grep -q ' nuw ' "$OUT_DIR/smoke.ll"
grep -q 'br i1 %flag' "$OUT_DIR/bool_switch.ll"
grep -q 'icmp ugt i32 %x, 10' "$OUT_DIR/bool_switch.ll"
grep -q 'alloca i32' "$OUT_DIR/loops.ll"
grep -q 'store i32' "$OUT_DIR/loops.ll"
grep -q 'load i32' "$OUT_DIR/loops.ll"
grep -q 'br label %while_cond' "$OUT_DIR/loops.ll"
grep -q '@llvm.usub.with.overflow.i32' "$OUT_DIR/loops.ll"
grep -q 'call void @mc_trap_DivideByZero()' "$OUT_DIR/div_rem.ll"
grep -q 'call void @mc_trap_IntegerOverflow()' "$OUT_DIR/div_rem.ll"
grep -q 'sdiv i32' "$OUT_DIR/div_rem.ll"
grep -q 'srem i32' "$OUT_DIR/div_rem.ll"
grep -q 'udiv i32' "$OUT_DIR/div_rem.ll"
grep -q 'urem i32' "$OUT_DIR/div_rem.ll"
grep -q 'switch_end' "$OUT_DIR/joins.ll"
grep -q 'br label %switch_end' "$OUT_DIR/joins.ll"
grep -q 'store i32' "$OUT_DIR/joins.ll"
grep -q 'define i32 @load_ptr(ptr %p)' "$OUT_DIR/pointers.ll"
grep -q 'load i32, ptr %p' "$OUT_DIR/pointers.ll"
grep -q 'store i32 %value, ptr %p' "$OUT_DIR/pointers.ll"
grep -q 'store ptr %x.addr, ptr %p.addr' "$OUT_DIR/pointers.ll"
grep -q '@shared = global i32 7' "$OUT_DIR/globals.ll"
grep -q '@flag = global i1 0' "$OUT_DIR/globals.ll"
grep -q '@shared_ptr = global ptr @shared' "$OUT_DIR/globals.ll"
grep -q 'load i32, ptr @shared' "$OUT_DIR/globals.ll"
grep -q 'store i32 %value, ptr @shared' "$OUT_DIR/globals.ll"
grep -q 'alloca \[2 x i32\]' "$OUT_DIR/arrays.ll"
grep -q 'getelementptr inbounds \[2 x i32\]' "$OUT_DIR/arrays.ll"
grep -q 'call void @mc_trap_Bounds()' "$OUT_DIR/arrays.ll"
grep -q 'store ptr %t' "$OUT_DIR/arrays.ll"
grep -q 'alloca { i32, i32 }' "$OUT_DIR/structs.ll"
grep -q 'getelementptr inbounds { i32, i32 }' "$OUT_DIR/structs.ll"
grep -q 'store i32 %value, ptr %t' "$OUT_DIR/structs.ll"
grep -q 'store ptr %t' "$OUT_DIR/structs.ll"
grep -q '@values = global \[2 x i32\] \[i32 10, i32 20\]' "$OUT_DIR/aggregate_globals.ll"
grep -q '@pair = global { i32, i32 } { i32 1, i32 2 }' "$OUT_DIR/aggregate_globals.ll"
grep -q 'getelementptr inbounds \[2 x i32\], ptr @values' "$OUT_DIR/aggregate_globals.ll"
grep -q 'getelementptr inbounds { i32, i32 }, ptr @pair' "$OUT_DIR/aggregate_globals.ll"
grep -q 'define { i32, i32 } @make_pair()' "$OUT_DIR/aggregate_abi.ll"
grep -q 'define i32 @take_pair({ i32, i32 } %pair)' "$OUT_DIR/aggregate_abi.ll"
grep -q 'call { i32, i32 } @make_pair()' "$OUT_DIR/aggregate_abi.ll"
grep -q 'call i32 @take_pair({ i32, i32 }' "$OUT_DIR/aggregate_abi.ll"
grep -q 'define \[2 x i32\] @make_row()' "$OUT_DIR/aggregate_abi.ll"
grep -q 'call i32 @take_row(\[2 x i32\]' "$OUT_DIR/aggregate_abi.ll"
