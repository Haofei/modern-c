llvm-as "$OUT_DIR/div_rem.ll" -o "$OUT_DIR/div_rem.bc"
"$MCC" emit-llvm tests/llvm/joins.mc > "$OUT_DIR/joins.ll"
llvm-as "$OUT_DIR/joins.ll" -o "$OUT_DIR/joins.bc"
