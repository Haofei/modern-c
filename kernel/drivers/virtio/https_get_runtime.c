// HTTPS-GET runtime: the REAL TLS bridge. Platform glue (virtio-mmio discovery, a
// bump DMA allocator, the vring memory, a virtio-rng entropy source, the clock seam)
// that drives a REAL BearSSL TLS 1.2 client over the MC kernel's hand-rolled TCP
// (tests/qemu/tls/tls_demo.mc) under QEMU user networking, fetching real HTTPS
// content and verifying the decrypted response.
//
// The crux is THE BRIDGE: BearSSL's record layer is wired to our TCP via two
// callbacks -- low_write -> tls_send (one PSH|ACK segment) and low_read -> tls_recv
// (the next inbound data segment, ACKed). The TLS engine, the X.509 minimal
// validator, the trust anchor(s), the injected entropy and the validation time are
// all set up here; then br_sslio_* runs a genuine handshake + an HTTPS GET and we
// check the decrypted body for the expected token.
//
// Compile-time parameters (the test scripts override via -D):
//   MC_BUILD_EPOCH : unix seconds, threaded in for X.509 validity (the clock seam).
//   HTTPS_PORT     : the TCP port to connect to (local test). Ignored when TLS_GOOGLE.
//   TLS_SERVERNAME : SNI / cert-name to validate against ("host.test" or "google.com").
//   TLS_HOSTHDR    : the HTTP Host: header value.
//   TLS_GOOGLE     : if defined, resolve TLS_DNSHOST via DNS (DNS_SERVER_IP) and
//                    connect to the resolved IP on :443 (the real-google path).
//   TLS_DNSHOST    : the name to resolve when TLS_GOOGLE (e.g. "google.com").
//   DNS_SERVER_IP  : the DNS server, host-order u32 (TLS_GOOGLE only).
// The trust anchor C is included (google_ta.c when TLS_GOOGLE, else local_ta.c); both
// define `TAs[]` + `TAs_NUM`.
#include <stdint.h>
#include <stddef.h>

#include "bearssl.h"

