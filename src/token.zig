    invalid,
    identifier,
    integer_literal,
    float_literal,
    string_literal,
    char_literal,

    kw_alignof,
    kw_asm,
    kw_assert,
    kw_atomic,
    kw_bool,
    kw_break,
    kw_comptime,
    kw_const,
    kw_continue,
    kw_defer,
    kw_else,
    kw_enum,
    kw_err,
    kw_export,
    kw_extern,
    kw_false,
    kw_fn,
    kw_for,
    kw_if,
    kw_let,
    kw_match,
    kw_mut,
    kw_never,
    kw_null,
    kw_ok,
    kw_open,
    kw_overlay,
    kw_packed,
    kw_pub,
    kw_return,
    kw_sat,
    kw_serial,
    kw_sizeof,
    kw_struct,
    kw_switch,
    kw_true,
    kw_type,
    kw_union,
    kw_uninit,
    kw_unsafe,
    kw_unreachable,
    kw_use,
    kw_var,
    kw_void,
    kw_while,
    kw_wrap,

    l_paren,
    r_paren,
